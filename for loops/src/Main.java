public class Main {
    public static void main(String[] args) {

        // for loop = executes a block of code a limited amount of times. While loop is infinite.

        // 3 parts to for loop condition 1.(int index = 0)  2.(i<10;) - when the index is less than 10. 3. (i++) increment the index after each iteration.

        // i = index in loop

        for(int index = 10; index>=0; index-=2){
            System.out.println(index);

        }

        System.out.println("Happy New Year");


    }
}